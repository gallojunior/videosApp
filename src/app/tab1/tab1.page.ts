      cartaz:
        'https://www.themoviedb.org/t/p/w600_and_h900_bestv2/wllzjZxg4ynlAm5xmOICJ2uHOPJ.jpg',
      generos: ['Ação', 'Aventura', 'Ficção científica'],
      pagina: '/godzilla-vs-king-kong',
